export default function TeacherDashboardLayout({ children }) {
  const { user, logout, loading } = useAuth();
  const router = useRouter();

  useEffect(() => {
    if (!loading && (!user || user.role !== 'teacher')) {
      router.push('/login/teacher');
    }
  }, [user, loading, router]);

  if (loading || !user) return null;

  return (
    <DashboardLayout 
      user={user} 
      logout={logout} 
      menuItems={menuItems} 
      role="Teacher"
    >
      {children}
    </DashboardLayout>
  );
}
